class SwapUsingVariable{
     public static void main(String[] args){
     int a=74;
     int b=24;
     System.out.println("Before Swapping :" + a +" and b is" +b);

    int c;
    c=a;
    a=b;
    b=c;

    System.out.println("After Swapping :" + a +" and b is" +b);

           
    }
}